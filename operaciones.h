int mov(int a, int b);
int add(int a, int b);
int sub(int a, int b);
int mul(int a, int b);
int divi(int a, int b);
int inc(int a);
int dec(int a);

int mov(int a, int b){
    return a=b;
}

int add(int a, int b){
    return a+b;
}

int sub(int a, int b){
    return a-b;
}

int mul(int a, int b){
    return a*b;
}

int divi(int a, int b){
    return a/b;
}

int inc(int a){
    a++;
    return a;
}

int dec(int a){
    a--;
    return a;
}






